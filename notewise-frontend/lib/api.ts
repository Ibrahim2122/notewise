export async function getSources(workspaceId: string): Promise<Source[]> {
  const res = await fetch(`${API_BASE}/workspaces/${workspaceId}/sources`, {
    headers: await authHeaders(),
  });
  if (!res.ok)
    throw new Error(
      `GET /workspaces/${workspaceId}/sources failed: ${res.status}`,
    );
  return (await res.json()).map(normaliseSource);
}

export async function createSource(
  workspaceId: string,
  title: string,
  sourceType: Source["source_type"],
  mimeType: string,
  originalFilename: string,
): Promise<Source> {
  const res = await fetch(`${API_BASE}/workspaces/${workspaceId}/sources`, {
    method: "POST",
    headers: await authHeaders(),
    body: JSON.stringify({
      title,
      source_type: sourceType,
      mime_type: mimeType,
      original_filename: originalFilename,
    }),
  });
  if (!res.ok)
    throw new Error(
      `POST /workspaces/${workspaceId}/sources failed: ${res.status}`,
    );
  return normaliseSource(await res.json());
}

