export function uploadFileToBlob(
  uploadUrl: string,
  file: File,
  onProgress?: (pct: number) => void,
): Promise<void> {
  // Direct PUT to Azure Blob Storage — no Authorization header here,
  // the SAS token embedded in uploadUrl is the credential.
  return new Promise((resolve, reject) => {
    const xhr = new XMLHttpRequest();
    xhr.open("PUT", uploadUrl);
    xhr.setRequestHeader("x-ms-blob-type", "BlockBlob");
    xhr.setRequestHeader(
      "Content-Type",
      file.type || "application/octet-stream",
    );
    if (onProgress) {
      xhr.upload.onprogress = (e) => {
        if (e.lengthComputable)
          onProgress(Math.round((e.loaded / e.total) * 100));
      };
    }
    xhr.onload = () => {
      if (xhr.status >= 200 && xhr.status < 300) resolve();
      else reject(new Error(`Blob upload failed: ${xhr.status}`));
    };
    xhr.onerror = () => reject(new Error("Blob upload network error"));
    xhr.send(file);
  });
}

export async function completeUpload(
  sourceId: string,
  blobPath: string,
  mimeType: string,
  originalFilename: string,
): Promise<Partial<Source>> {
  const res = await fetch(`${API_BASE}/sources/${sourceId}/upload-complete`, {
    method: "POST",
    headers: await authHeaders(),
    body: JSON.stringify({
      blob_path: blobPath,
      mime_type: mimeType,
      original_filename: originalFilename,
    }),
  });
  if (!res.ok)
    throw new Error(
      `POST /sources/${sourceId}/upload-complete failed: ${res.status}`,
    );
  // Backend returns only {id, storage_uri, status} — caller merges onto full source
  return res.json();
}

/** Convenience: run the full 3-step upload and return the finalised source. */
export async function uploadSource(
  workspaceId: string,
  file: File,
  onProgress?: (pct: number) => void,
): Promise<Source> {
  const sourceType: Source["source_type"] = file.type.includes("pdf")
    ? "pdf"
    : file.type.includes("audio")
      ? "audio"
      : "pdf";

  const source = await createSource(
    workspaceId,
    file.name,
    sourceType,
    file.type,
    file.name,
  );
  const { upload_url, blob_path } = await getUploadUrl(source.id);
  await uploadFileToBlob(upload_url, file, onProgress);
  const completed = await completeUpload(
    source.id,
    blob_path,
    file.type || "application/octet-stream",
    file.name,
  );
  // completeUpload returns only {id, storage_uri, status} — merge back onto
  // the full source so fields like source_type/type/name are never undefined.
  return { ...source, ...completed };
}

