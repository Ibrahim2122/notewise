export async function createWorkspace(name: string): Promise<Workspace> {
  const res = await fetch(
    `${API_BASE}/workspaces?name=${encodeURIComponent(name)}`,
    { method: "POST", headers: await authHeaders() },
  );
  if (!res.ok) throw new Error(`POST /workspaces failed: ${res.status}`);
  return normaliseWorkspace(await res.json());
}

export async function getWorkspace(id: string): Promise<Workspace | null> {
  const res = await fetch(`${API_BASE}/workspaces/${id}`, {
    headers: await authHeaders(),
  });
  if (res.status === 404) return null;
  if (!res.ok) throw new Error(`GET /workspaces/${id} failed: ${res.status}`);
  return normaliseWorkspace(await res.json());
}

export async function deleteWorkspace(id: string): Promise<void> {
  const res = await fetch(`${API_BASE}/workspaces/${id}`, {
    method: "DELETE",
    headers: await authHeaders(),
  });
  if (!res.ok)
    throw new Error(`DELETE /workspaces/${id} failed: ${res.status}`);
}

