export type SourceStatus = "pending" | "processing" | "done" | "failed";

export interface Source {
  id: string;
  workspace_id: string;
  source_type: "pdf" | "audio" | "text" | "url";
  title: string;
  /** UI alias used by job-status-panel and source-list */
  name: string;
  /** UI alias used by job-status-panel */
  type: string;
  /** UI alias used by job-status-panel */
  addedAt: string;
  status: SourceStatus;
  storage_uri: string | null;
  text_content: string | null;
  url: string | null;
  mime_type: string | null;
  original_filename: string | null;
  created_at: string;
  latest_summary: string | null;
}

export interface WorkspaceAssets {
  /**
   * TSX component source code generated by Gemini and stored in artifacts.content.
   * Null if not yet generated.
   */
  deepdive: string | null;
}

export interface Job {
  id: string;
  type: "deepdive" | "summary" | "ingest";
  status: "queued" | "running" | "completed" | "failed";
  updatedAt: string;
}

export interface Workspace {
  id: string;
  name: string;
  description: string | null;
  created_at: string;
  sources: Source[];
  summary: string | null;
  latest_summary: string | null;
  assets: WorkspaceAssets;
  jobs: Job[];
}

export interface WorkspaceCard {
  id: string;
  name: string;
  description: string | null;
  source_count: number;
  has_processing: boolean;
  latest_summary: string | null;
  created_at: string;
}

export interface UploadUrlResponse {
  upload_url: string;
  blob_path: string;
}

export interface SourceStatusResponse {
  id: string;
  status: SourceStatus;
  latest_summary: string | null;
}

// ---------------------------------------------------------------------------
// Normalise helpers
// ---------------------------------------------------------------------------

// eslint-disable-next-line @typescript-eslint/no-explicit-any
function normaliseSource(s: any): Source {
  return {
    ...s,
    name: s.title ?? s.original_filename ?? "Untitled",
    addedAt: s.created_at,
    type: s.source_type,
  };
}

// eslint-disable-next-line @typescript-eslint/no-explicit-any
function normaliseWorkspace(raw: any): Workspace {
  return {
    id: raw.id,
    name: raw.name,
    description: raw.description ?? null,
    created_at: raw.created_at,
    sources: (raw.sources ?? []).map(normaliseSource),
    summary: raw.latest_summary ?? null,
    latest_summary: raw.latest_summary ?? null,
    // Backend returns latest_deepdive: TSX string | null
    assets: { deepdive: raw.latest_deepdive ?? null },
    jobs: [],
  };
}

function mapJobStatus(raw: string): Job["status"] {
  switch (raw) {
    case "pending":
      return "queued";
    case "processing":
      return "running";
    case "done":
      return "completed";
    case "failed":
      return "failed";
    default:
      return "queued";
  }
}

