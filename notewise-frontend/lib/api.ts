/**
 * POST /workspaces/{wid}/deepdive
 *
 * Tells the backend to (re-)generate the DEEPDIVE artifact for this workspace.
 * The backend queues a background task, updates the deepdive artifact in DB,
 * and returns a job record.
 */
export async function triggerAssetGeneration(
  workspaceId: string,
  _assetType: "deepdive",
): Promise<Job> {
  const res = await fetch(`${API_BASE}/workspaces/${workspaceId}/deepdive`, {
    method: "POST",
    headers: await authHeaders(),
  });
  if (!res.ok)
    throw new Error(
      `POST /workspaces/${workspaceId}/deepdive failed: ${res.status}`,
    );
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const raw: any = await res.json();
  return {
    id: raw.id ?? raw.job_id ?? `deepdive-${workspaceId}`,
    type: "deepdive",
    status: mapJobStatus(raw.status ?? "pending"),
    updatedAt: raw.updated_at ?? raw.created_at ?? new Date().toISOString(),
  };
}

/**
 * Polls job status by re-fetching the workspace and synthesising a Job list.
 * Once `POST /workspaces/{wid}/jobs` exists on the backend this can be replaced.
 */
export async function pollJobStatus(workspaceId: string): Promise<Job[]> {
  const workspace = await getWorkspace(workspaceId);
  if (!workspace) return [];

  // Synthesise one job entry per type based on workspace state
  const jobs: Job[] = [];

  const hasProcessing = workspace.sources.some(
    (s) => s.status === "pending" || s.status === "processing",
  );
  if (hasProcessing) {
    jobs.push({
      id: `synth-ingest-${workspaceId}`,
      type: "ingest",
      status: "running",
      updatedAt: new Date().toISOString(),
    });
  }

  jobs.push({
    id: `synth-deepdive-${workspaceId}`,
    type: "deepdive",
    status: workspace.assets.deepdive ? "completed" : "queued",
    updatedAt: workspace.created_at,
  });

  return jobs;
}
