const API_BASE = process.env.NEXT_PUBLIC_API_URL ?? "http://localhost:8000";

// ---------------------------------------------------------------------------
// Token getter — injected by AuthProvider so this file has no MSAL dependency
// ---------------------------------------------------------------------------

let _getAccessToken: (() => Promise<string | null>) | null = null;

/**
 * Called once by AuthProvider after MSAL is initialised.
 * Provides a way to silently acquire the current access token on every request.
 */
export function setAccessTokenGetter(fn: () => Promise<string | null>) {
  _getAccessToken = fn;
}

async function authHeaders(
  extra?: Record<string, string>,
): Promise<Record<string, string>> {
  const token = _getAccessToken ? await _getAccessToken() : null;
  if (!token) throw new Error("Not authenticated — no access token available");
  return {
    "Content-Type": "application/json",
    Authorization: `Bearer ${token}`,
    ...extra,
  };
}

